# Calculation

# Addition
print('Addition------')
num1 = 12;
num2 = 13;
print('the value of num1 is : ',num1)
print('the value of num2 is : ',num2)
sum = num1 + num2;
print('the sum of num1 and num2 is : ',sum)
print('---------------')

# Subtraction
print('Subtraction------')
diff = num1 - num2
print('the difference between num1 and num2 is : ',diff)
print('---------------')

# Multiplication
print('Multiplication------')
num1 = 10
num2 = 20
multi = num1 * num2
print('the value of num1 is now ',num1)
print('the value of num2 is now :', num2)
print('the multiplication of num1 and num2 is : ',multi)
print('---------------')

